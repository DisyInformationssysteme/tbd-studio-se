package org.talend.spark.distribution.spark311.modulegroup;

import java.util.HashSet;
import java.util.Set;

import org.talend.hadoop.distribution.DistributionModuleGroup;
import org.talend.hadoop.distribution.constants.ModuleGroupName;
import org.talend.spark.distribution.spark311.Spark311Distribution;

public class Spark311SparkCompileModuleGroup {

    public static Set<DistributionModuleGroup> getModuleGroups() {
        Set<DistributionModuleGroup> hs = new HashSet<>();
        hs.add(new DistributionModuleGroup(ModuleGroupName.SPARK_COMPILE.get(Spark311Distribution.VERSION), false));
        return hs;
    }
}
